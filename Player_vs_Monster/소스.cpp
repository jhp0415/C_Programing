void print_screen()
{
    int i, j;
    for (i = 0; i < map_hight; i++)
    {
        for (j = 0; j < map_width; j++)
        {
            if (i == 0 || i == map_hight - 1 || j == 0 || j == map_width - 1 || j == map_width/2 )
                map[i][j] = 1;
            else
                map[i][j] = 0;
        }
    }
    //맵 출력함수
    for (int i = 0; i < MAP_HIGHT; i++)
    {
        for (int j = 0; j < MAP_WIDTH; j++)
        {
            if (map[i][j] == 1)
                printf("□");  //문자는 2칸을 차지한다
            else if (map[i][j] == 0)
                printf("  ");  //공백을 2개 한다
        }
        printf("\n");
    }
}

